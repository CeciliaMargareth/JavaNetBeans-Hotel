    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    try {
        st = cn.createStatement();

        // Mengambil ImageIcon dari label
        ImageIcon icon = (ImageIcon) lbl_photo.getIcon();
        
        if (icon != null) {
            // Mengonversi ImageIcon ke byte array
            BufferedImage bi = new BufferedImage(icon.getIconWidth(), icon.getIconHeight(), BufferedImage.TYPE_INT_ARGB);
            Graphics g = bi.createGraphics();
            g.drawImage(icon.getImage(), 0, 0, null);
            g.dispose();

            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            ImageIO.write(bi, "png", bos);
            byte[] imageBytes = bos.toByteArray();

            // Menyimpan data ke database termasuk gambar
            PreparedStatement ps = cn.prepareStatement("INSERT INTO fasilitas_umum (namaFasilitas, keterangan, image) VALUES (?, ?, ?)");
            ps.setString(1, namaFasilitas.getText());
            ps.setString(2, keterangan.getText());
            ps.setBytes(3, imageBytes);
            ps.executeUpdate();

            tampilData("");
            JOptionPane.showMessageDialog(null, "Simpan Berhasil");
            namaFasilitas.setText("");
            keterangan.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Anda belum memilih gambar!");
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

