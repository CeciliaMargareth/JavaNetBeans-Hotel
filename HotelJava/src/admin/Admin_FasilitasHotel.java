    public void tampilData(String where) {
    try {
        st = cn.createStatement();
        tabModel.getDataVector().removeAllElements();
        rs = st.executeQuery("SELECT * FROM fasilitas_umum " + where);

        while (rs.next()) {
            // Mengambil nama file gambar dari hasil query
            String namaFasilitas = rs.getString("namaFasilitas");
            String keterangan = rs.getString("keterangan");
            String namaFileGambar = rs.getString("image");

            // Menambahkan data ke dalam tabel
            Object[] data = {namaFasilitas, keterangan, namaFileGambar};
            tabModel.addRow(data);
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}

    
